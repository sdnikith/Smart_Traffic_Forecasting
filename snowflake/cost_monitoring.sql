SELECT 
    WAREHOUSE_NAME,
    DATE_TRUNC('day', START_TIME) AS usage_date,
    SUM(CREDITS_USED) AS total_credits,
    SUM(CREDITS_USED) * 3.00 AS estimated_cost_usd,
    COUNT(*) AS query_count,
    AVG(TOTAL_ELAPSED_TIME) / 1000 AS avg_query_seconds,
    MAX(TOTAL_ELAPSED_TIME) / 1000 AS max_query_seconds,
    AVG(QUEUED_OVERLOAD_TIME) / 1000 AS avg_queue_time
FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_METERING_HISTORY
WHERE START_TIME >= DATEADD('day', -30, CURRENT_TIMESTAMP())
GROUP BY WAREHOUSE_NAME, DATE_TRUNC('day', START_TIME);


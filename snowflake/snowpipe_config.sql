-- Snowpipe for auto-ingestion when new files land in S3
CREATE OR REPLACE PIPE TRAFFIC_DB.RAW.TRAFFIC_PIPE
  AUTO_INGEST = TRUE
  AS
  COPY INTO TRAFFIC_DB.RAW.TRAFFIC_EVENTS
  FROM @TRAFFIC_DB.RAW.S3_TRAFFIC_STAGE
  FILE_FORMAT = (TYPE = 'PARQUET')
  MATCH_BY_COLUMN_NAME = CASE_INSENSITIVE
  ON_ERROR = 'CONTINUE'
  PURGE = TRUE
  COMMENT = 'Auto-ingest traffic events from Delta Lake';


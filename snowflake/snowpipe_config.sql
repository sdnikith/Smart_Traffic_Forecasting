-- Create notification integrations for pipe monitoring
CREATE OR REPLACE NOTIFICATION INTEGRATION TRAFFIC_PIPE_NOTIFICATION
  TYPE = QUEUE
  ENABLED = TRUE
  NOTIFICATION_PROVIDER = AWS_SNS
  AWS_SNS_TOPIC_ARN = 'arn:aws:sns:us-east-1:123456789012:traffic-pipe-notifications'
  COMMENT = 'Notifications for Snowpipe activity';

-- Grant pipe monitoring
GRANT MONITOR ON PIPE TRAFFIC_DB.RAW.TRAFFIC_PIPE TO ROLE SYSADMIN;
GRANT MONITOR ON PIPE TRAFFIC_DB.RAW.WEATHER_PIPE TO ROLE SYSADMIN;
GRANT MONITOR ON PIPE TRAFFIC_DB.RAW.HOLIDAYS_PIPE TO ROLE SYSADMIN;
GRANT MONITOR ON PIPE TRAFFIC_DB.RAW.SENSORS_PIPE TO ROLE SYSADMIN;

-- Check pipe status
SELECT 
    'TRAFFIC_EVENTS' AS pipe_name,
    SYSTEM$PIPE_STATUS('TRAFFIC_DB.RAW.TRAFFIC_PIPE') AS status
UNION ALL
SELECT 
    'WEATHER_UPDATES' AS pipe_name,
    SYSTEM$PIPE_STATUS('TRAFFIC_DB.RAW.WEATHER_PIPE') AS status
UNION ALL
SELECT 
    'HOLIDAYS' AS pipe_name,
    SYSTEM$PIPE_STATUS('TRAFFIC_DB.RAW.HOLIDAYS_PIPE') AS status
UNION ALL
SELECT 
    'SENSORS' AS pipe_name,
    SYSTEM$PIPE_STATUS('TRAFFIC_DB.RAW.SENSORS_PIPE') AS status;

-- Set default roles
ALTER USER traffic_loader_user SET DEFAULT_ROLE = TRAFFIC_LOADER;
ALTER USER dbt_user SET DEFAULT_ROLE = TRAFFIC_TRANSFORMER;
ALTER USER tableau_user SET DEFAULT_ROLE = TRAFFIC_ANALYST;
ALTER USER ml_user SET DEFAULT_ROLE = TRAFFIC_ML_CONSUMER;
ALTER USER admin_user SET DEFAULT_ROLE = TRAFFIC_ADMIN;

-- Create network policies for security
CREATE OR REPLACE NETWORK POLICY TRAFFIC_ALLOWED_IPS AS (src_ip VARCHAR) RETURNS BOOLEAN ->
  CASE 
    WHEN CURRENT_ROLE() IN ('TRAFFIC_ADMIN') THEN TRUE
    WHEN src_ip LIKE '192.168.%' THEN TRUE  -- Internal network
    WHEN src_ip LIKE '10.%' THEN TRUE        -- AWS internal
    ELSE FALSE
  END;

-- Apply network policies to warehouses
ALTER WAREHOUSE TRAFFIC_LOAD_WH SET NETWORK_POLICY = TRAFFIC_ALLOWED_IPS;
ALTER WAREHOUSE TRAFFIC_TRANSFORM_WH SET NETWORK_POLICY = TRAFFIC_ALLOWED_IPS;
ALTER WAREHOUSE TRAFFIC_ANALYTICS_WH SET NETWORK_POLICY = TRAFFIC_ALLOWED_IPS;
ALTER WAREHOUSE TRAFFIC_ML_WH SET NETWORK_POLICY = TRAFFIC_ALLOWED_IPS;

-- Log RBAC setup
SELECT 'RBAC and security policies setup completed' AS status;
